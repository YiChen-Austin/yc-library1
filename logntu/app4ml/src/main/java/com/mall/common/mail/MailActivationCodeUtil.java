package com.mall.common.mail;

import java.util.UUID;

/**
 * 邮箱激活码生成。
 * @author sol
 */
public class MailActivationCodeUtil
{
	/**
	 * 生成邮箱激活码。
	 * @author sol
	 * @return 邮箱激活码。随机字符串，长度小于等于32字节。
	 */
	public static String generate() {
		UUID uuid = UUID.randomUUID();
		return uuid.toString();
	}
}
