public class MobEnum {
	/**
	 * url（或界面）跳转枚举，主要适用于Android和IOS
	 */
	public enum Url2TypeEnum {
		/**
		 * index:0,name:跳转产品列表
		 */
		ToProList("跳转产品列表", 0),
		/**
		 * index:1,name:跳转产品详情
		 */
		ToProduct("跳转产品详情", 1),
		/**
		 * index:2,name:跳转活动表
		 */
		ToActivity("跳转活动表", 2),
		/**
		 * index:3,name:跳转WAP网页
		 */
		ToWap("跳转WAP网页", 3),
		/**
		 * index:3,name:跳转WAP网页
		 */
		ToNo3cPro("跳转非3C产品", 4);
		// 成员变量
		public String name;
		public int index;

		// 构造方法
		private Url2TypeEnum(String name, int index) {
			this.name = name;
			this.index = index;
		}
	}

	/**
	 * 网络请求数据状态
	 */
	public enum DataStatusEnum {
		/**
		 * index:0,name:成功
		 */
		SUCCESS("成功", 0),
		/**
		 * index:1,name:服务器错
		 */
		F_SERVER_ERROR("服务器错", 1),
		/**
		 * index:2,name:无数据
		 */
		F_NO_DATA("无数据", 2),
		/**
		 * index:3,name:其他错误
		 */
		F_OTHER_ERROR("其他错误", 3);
		// 成员变量
		public String name;
		public int index;

		// 构造方法
		private DataStatusEnum(String name, int index) {
			this.name = name;
			this.index = index;
		}
	}
}
