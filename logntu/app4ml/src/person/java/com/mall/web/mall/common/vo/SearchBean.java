package com.mall.web.mall.common.vo;

/**
 * 搜索条件
 * 
 * @author ty
 * 
 */
public class SearchBean {
	private String keyword;
	private String app;
	private String searchType;
	private String act;
	private String submit;
	private String zoneId;
	private String regionId;
	private String mcategoryId;
	private String scategoryId;
	private String longitude;// 经度
	private String latitude;// 纬度
	private String radius;// 半径
	private String subRegion;// 更细区域

	private String order = "dist";
	private String oDirect = "asc";
	private int distance = 5000;

	private String seoTitle;
	private String seoKeyword;
	private String seoDescription;

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public String getApp() {
		return app;
	}

	public void setApp(String app) {
		this.app = app;
	}

	public String getAct() {
		return act;
	}

	public void setAct(String act) {
		this.act = act;
	}

	public String getSubmit() {
		return submit;
	}

	public void setSubmit(String submit) {
		this.submit = submit;
	}

	public String getRegionId() {
		return regionId;
	}

	public void setRegionId(String regionId) {
		this.regionId = regionId;
	}

	public String getSearchType() {
		return searchType;
	}

	public void setSearchType(String searchType) {
		this.searchType = searchType;
	}

	public String getScategoryId() {
		return scategoryId;
	}

	public void setScategoryId(String scategoryId) {
		this.scategoryId = scategoryId;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getSubRegion() {
		return subRegion;
	}

	public void setSubRegion(String subRegion) {
		this.subRegion = subRegion;
	}

	public String getMcategoryId() {
		return mcategoryId;
	}

	public void setMcategoryId(String mcategoryId) {
		this.mcategoryId = mcategoryId;
	}

	public String getZoneId() {
		return zoneId;
	}

	public void setZoneId(String zoneId) {
		this.zoneId = zoneId;
	}

	public String getRadius() {
		return radius;
	}

	public void setRadius(String radius) {
		this.radius = radius;
	}

	public String getOrder() {
		return order;
	}

	public void setOrder(String order) {
		if (order == null)
			return;
		this.order = order;
	}

	public int getDistance() {
		return distance;
	}

	public void setDistance(int distance) {
		this.distance = distance;
	}

	public String getoDirect() {
		return oDirect;
	}

	public void setoDirect(String oDirect) {
		if (oDirect == null)
			return;
		this.oDirect = oDirect;
	}

	public String getSeoTitle() {
		return seoTitle;
	}

	public void setSeoTitle(String seoTitle) {
		this.seoTitle = seoTitle;
	}

	public String getSeoKeyword() {
		return seoKeyword;
	}

	public void setSeoKeyword(String seoKeyword) {
		this.seoKeyword = seoKeyword;
	}

	public String getSeoDescription() {
		return seoDescription;
	}

	public void setSeoDescription(String seoDescription) {
		this.seoDescription = seoDescription;
	}

}
